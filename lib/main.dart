  Widget build(BuildContext context) {
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      theme: ThemeData(brightness: Brightness.light),
      initialRoute: "/login",
      routes: {
        "/login": (context) => const LoginScreen(),
      },
    );
  }
}
